package scanner.ex;

public class ScannerEx5 {
    public static void main(String[] args) {
        // a, b 위치 바꾸기
        int a = 10;
        int b = 20;
        int temp;

        // 시작: 코드를 작성
        temp = a; // 10
        a = b;
        b = temp;
        // 종료: 코드를 작성

        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }
}
